<?php

namespace app\models;

use function alias;
use app\traits\ImageTrait;
use function date;
use const false;
use Yii;

/**
 * This is the model class for table "articles".
 *
 * @property int $id
 * @property string $title
 * @property string $description
 * @property string $content
 * @property string $date
 * @property string $image
 * @property int $viewed
 * @property int $user_id
 * @property int $status
 * @property int $category_id
 */
class Article extends \yii\db\ActiveRecord
{
    use ImageTrait;

    public $image_path;
    public $preview;

    const STATUS_WAIT = 0;
    const STATUS_PUBLISHED = 1;

    public function beforeSave($insert)
    {
        $this->user_id = Yii::$app->user->id;
        $this->alias = alias($this->title);
        return parent::beforeSave($insert); // TODO: Change the autogenerated stub
    }

    public function afterFind()
    {
        $this->image_path = '@web/' . $this->buildFilePath('article', $this->image);
        parent::afterFind(); // TODO: Change the autogenerated stub
    }

    /**
     * {@inheritdoc}
     */
    public static function tableName()
    {
        return 'articles';
    }

    /**
     * {@inheritdoc}
     */
    public function rules()
    {
        return [
            [['title'], 'required'],
            [['status'], 'integer', 'min' => self::STATUS_WAIT, 'max' => self::STATUS_PUBLISHED],
            [['status'], 'default', 'value' =>  self::STATUS_WAIT],
            [['title', 'description', 'content'], 'string'],
            [['date'], 'default', 'value' => date('Y-m-d')],
            [['viewed'], 'default', 'value' => 0],
            [['preview'], 'file', 'extensions' => 'png,jpg'],
            [['category_id'], 'integer'],
            [['category_id'], 'exist', 'targetRelation' => 'category'
                /*'targetClass' => Article::class,
                'targetAttribute' => ['category_id' => 'id']*/
            ],
            [['likes_count'], 'integer'],
            [['likes_count'], 'default', 'value' =>  0],
        ];
    }

    /**
     * {@inheritdoc}
     */
    public function attributeLabels()
    {
        return [
            'id' => 'ID',
            'title' => 'Заголовок',
            'description' => 'Описание',
            'content' => 'Содержание',
            'date' => 'Дата создания',
            'image' => 'Изображение',
//            'viewed' => 'Viewed',
//            'user_id' => 'User ID',
//            'status' => 'Status',
            'category_id' => 'Категория',
        ];
    }

   public function review()
   {
       $this->viewed += 1;
       $this->save(false);
   }

   public function publish()
   {
       $this->status = !$this->status;
       $this->save(false);
   }

   public function getCategory()
   {
       return $this->hasOne(Category::class, ['id' => 'category_id']);
   }

   public function getUser()
   {
       return $this->hasOne(User::class, ['id' => 'user_id']);
   }

   public function getComments()
   {
       return $this->hasMany(Comment::class, ['article_id' => 'id'])->where(['status' => Comment::STATUS_PUBLISHED]);
   }

   public function getLikes()
   {
       return $this->hasMany(Like::class, ['object_id' => 'id']);
   }

    public function addComment(Comment $comment)
    {
        $this->comments_count += 1;
        $this->save(false);
        $this->link('comments', $comment);
    }

    public function removeComment(Comment $comment)
    {
        $this->comments_count -= 1;
        $this->save(false);
        $comment->delete();
    }

    public function addLike(Like $like)
    {
        $this->likes_count += 1;
        $this->save(false);
        $this->link('likes', $like);
    }

    public function removeLike(Like $like)
    {
        $this->likes_count -= 1;
        $this->save(false);
        $like->delete();
    }

    public function getImagePath()
    {
        return '@web/' . $this->buildFilePath('article', $this['image']);
    }
}
